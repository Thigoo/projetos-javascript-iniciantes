/* Neste projeto vamos interagir com o comando prompt, coletando as informações e armazenando em suas respectivas variáveis */

const nome = prompt("Olá, qual é o seu nome?");
const idade = Number(prompt(`E a sua idade?`));

/* Em seguida o comando alert dispara uma mensagem conforme as informações dadas pelo usuário no prompt */
alert(`Seu nome é ${nome} e você tem ${idade} anos`);

